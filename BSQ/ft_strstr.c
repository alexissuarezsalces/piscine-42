#include "ft.h"

void	ft_putstr_str(char **str)
{
	int x;
	int y;

	x = 0;
	y = 0;
	while (str[x][y] != '\0')
	{
		y = 0;
		ft_putstr(str[y]);
		x++;
	}
}
